    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        Laboratorio lab ;
        DAOLaboratorio dao ;
        
        if( this.validar() == true){
            lab = this.crearEntidad();
            dao = new DAOLaboratorio();
            try{
                if( this.actual==null){
                    dao.registrar( lab);
                }else{
                    dao.modificar( lab);
                }
                this.listarLaboratorios();
                this.activarControles(false);
            }catch(Exception ex){
                JOptionPane.showMessageDialog(this, ex.getMessage());
            }
        }
    }//GEN-LAST:event_btnAceptarActionPerformed

    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        int fila = this.tblListado.getSelectedRow();
        
        if( fila> -1){
            this.actual = this.laboratorios.get(fila);
            this.presentarDatos();
        }else{
            JOptionPane.showMessageDialog(this, "Debe seleccionar un laboratorio");
        }
    }//GEN-LAST:event_btnModificarActionPerformed

