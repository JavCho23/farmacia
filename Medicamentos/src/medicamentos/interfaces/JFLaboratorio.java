    private void activarControles(boolean estado) {
        this.panLaboratorio.setEnabled(estado);
        this.lblNombre.setEnabled(estado);
        this.txtNombre.setEnabled(estado);
        this.chkVigente.setEnabled(estado);
        this.btnAceptar.setEnabled(estado);
        this.btnCancelar.setEnabled(estado);
        
        this.panListado.setEnabled(! estado);
        this.tblListado.setEnabled( ! estado);
        this.btnNuevo.setEnabled(! estado);
        this.btnModificar.setEnabled( ! estado);
        
        if( estado == true ){
            this.txtNombre.requestFocusInWindow();
        }else{
            this.tblListado.requestFocusInWindow();
        }
    }

    private void limpiarControles() {
        this.txtNombre.setText("");
        this.chkVigente.setSelected(true);
    }

    private boolean validar() {
        return true;
    }

    private Laboratorio crearEntidad() {
        Laboratorio lab  =new Laboratorio();
        
        lab.setNombre(this.txtNombre.getText());
        lab.setVigente(this.chkVigente.isSelected());
        if( this.actual  != null){
            lab.setCodigo(this.actual.getCodigo());
        }
        return lab;
    }

    private void listarLaboratorios() {
        DAOLaboratorio dao = new DAOLaboratorio();
        
        try{
            this.laboratorios = dao.listar();
            this.ltm.setLaboratorios(laboratorios);
        }catch(Exception ex){
            JOptionPane.showMessageDialog(this, "No se pudo listar los laboratorios");
        }
    }

    private void presentarDatos() {
        this.activarControles(true);
        this.txtNombre.setText(this.actual.getNombre());
        this.chkVigente.setSelected(this.actual .isVigente());
    }
