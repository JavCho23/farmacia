    private void presentarDatos() {
        DAOMedicamento dao = new DAOMedicamento();
        ComboBoxModel modeloCombo;
        
        try{
            this.actual = dao.leer(this.actual.getCodigo());
            if( this.actual != null){
                modeloCombo = this.cboLaboratorio.getModel();
                ((LaboratorioComboBoxModel)modeloCombo).seleccionar( this.actual.getLaboratorio());
                this.txtNombre.setText(this.actual.getNombre());
                this.spPrecio.setValue(this.actual.getPrecio());
                this.chkVigente.setSelected(this.actual.isVigente());
                this.activarControles(true);
            }else{
                JOptionPane.showMessageDialog(this, "No se encontró el medicamento");
            }
        }catch(Exception ex){
            JOptionPane.showMessageDialog(this, "No se pudo obtener la informaciòn del medicamento");
        }
    }

    private void activarControles(boolean estado) {
        this.panMedicamento.setEnabled(estado);
        this.lblLaboratorio.setEnabled(estado);
        this.cboLaboratorio.setEnabled(estado);
        this.lblNombre.setEnabled(estado);
        this.txtNombre.setEnabled(estado);
        this.lblPrecio.setEnabled(estado);
        this.spPrecio.setEnabled(estado);
        this.chkVigente.setEnabled(estado);

        this.panListado.setEnabled(!estado);
        this.lblMedicamento.setEnabled(!estado);
        this.txtMedicamento.setEnabled(!estado);
        this.btnListar.setEnabled(!estado);
        this.tblListado.setEnabled(!estado);
        this.btnNuevo.setEnabled(!estado);
        this.btnModificar.setEnabled(!estado);
        if (estado == true) {
            this.cboLaboratorio.requestFocusInWindow();
        } else {
            this.txtMedicamento.requestFocusInWindow();
        }
    }

    private void limpiarControles() {
        this.cboLaboratorio.setSelectedIndex(-1);
        this.txtNombre.setText("");
        this.spPrecio.setValue(1.0);
        this.chkVigente.setSelected(true);
    }

    private ComboBoxModel listarLaboratorios() {
        LaboratorioComboBoxModel modelo = new LaboratorioComboBoxModel();

        modelo.setLaboratorios(this.listarLaboratoriosVigentes());

        return modelo;
    }

    private List<Laboratorio> listarLaboratoriosVigentes() {
        DAOLaboratorio dao = new DAOLaboratorio();

        try {
            this.laboratorios = dao.listarVigentes();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "No se pudo obtener la lista de laboratorios");
        }

        return this.laboratorios;
    }
