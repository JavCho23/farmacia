    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        Medicamento med;
        DAOMedicamento dao;

        if (this.validar() == true) {
            med = this.crearEntidad();
            dao = new DAOMedicamento();
            try {
                if (this.actual == null) {
                    dao.registrar(med);
                } else {
                    dao.modificar(med);
                }
                this.activarControles(false);
                this.limpiarControles();           
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage());
            }
        }
    }//GEN-LAST:event_btnAceptarActionPerformed

    private Medicamento crearEntidad() {
        Medicamento med = new Medicamento();

        med.setLaboratorio(this.laboratorios.get(this.cboLaboratorio.getSelectedIndex()));
        med.setNombre(this.txtNombre.getText());
        med.setPrecio( Double.parseDouble( this.spPrecio.getValue().toString()));
        med.setVigente( this.chkVigente.isSelected());
        if( this.actual != null){
            med.setCodigo(this.actual.getCodigo());
        }
        
        return med;
    }

    private boolean validar() {
        return true;
    }

