    private void activarControles(boolean estado) {
        this.panPrincipioActivo.setEnabled(estado);
        this.lblNombre.setEnabled(estado);
        this.txtNombre.setEditable(estado);
        this.lblDescripcion.setEnabled(estado);
        this.txtDescripcion.setEditable(estado);
        this.chkVigente.setEnabled(estado);
        this.btnAceptar.setEnabled(estado);
        this.btnCancelar.setEnabled(estado);
        
        this.panListadoPA.setEnabled(! estado);
        this.tblListadoPA.setEnabled(! estado);
        this.btnNuevo.setEnabled(! estado);
        this.btnModificar.setEnabled(! estado);
        
        if(estado == true){
            this.txtNombre.requestFocusInWindow();
        }else{
            this.tblListadoPA.requestFocusInWindow();
        }
    }

    private void listarPrincipiosActivos() {
        DAOPrincipioActivo dao = new DAOPrincipioActivo();
        
        try {
            this.principiosActivos = dao.listar();
            this.ltpa.setPrincipiosActivos(principiosActivos);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "No se pudo listar los Principios Activos");
        }
    }
